package com.ia.web.Modal;

public class AddressComponents {

	private int addressComponentsId;
	private int addressDetailId;
	private String long_name;
	private String short_name;
	private String types;
	
	public int getAddressComponentsId() {
		return addressComponentsId;
	}
	public void setAddressComponentsId(int addressComponentsId) {
		this.addressComponentsId = addressComponentsId;
	}
	public int getAddressDetailId() {
		return addressDetailId;
	}
	public void setAddressDetailId(int addressDetailId) {
		this.addressDetailId = addressDetailId;
	}
	public String getLong_name() {
		return long_name;
	}
	public void setLong_name(String long_name) {
		this.long_name = long_name;
	}
	public String getShort_name() {
		return short_name;
	}
	public void setShort_name(String short_name) {
		this.short_name = short_name;
	}
	public String getTypes() {
		return types;
	}
	public void setTypes(String types) {
		this.types = types;
	}
}
