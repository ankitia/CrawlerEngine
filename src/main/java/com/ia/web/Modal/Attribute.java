package com.ia.web.Modal;

public class Attribute {
	private int market_variables;
	private AddressAttribute address;
	private int tech_install;
	private int key_contact;
	private int sku;
	private int url_status;
	private int social_data;
	
	
	public int getSocial_data() {
		return social_data;
	}
	public void setSocial_data(int social_data) {
		this.social_data = social_data;
	}
	public int getUrl_status() {
		return url_status;
	}
	public void setUrl_status(int url_status) {
		this.url_status = url_status;
	}
	public int getMarket_variables() {
		return market_variables;
	}
	public void setMarket_variables(int market_variables) {
		this.market_variables = market_variables;
	}
	 
	public AddressAttribute getAddress() {
		return address;
	}
	public void setAddress(AddressAttribute address) {
		this.address = address;
	}
	public int getTech_install() {
		return tech_install;
	}
	public void setTech_install(int tech_install) {
		this.tech_install = tech_install;
	}
	public int getKey_contact() {
		return key_contact;
	}
	public void setKey_contact(int key_contact) {
		this.key_contact = key_contact;
	}
	public int getSku() {
		return sku;
	}
	public void setSku(int sku) {
		this.sku = sku;
	}
	 
}
